var limit;
var start = 0;
var loc;
var correct;
var miss;
var score = -1;
var play = false;
var target = document.getElementById('target');
var correctLabel = document.getElementById('correct');
var missLabel = document.getElementById('miss');
var timerLabel = document.getElementById('timer');
var scoreLabel = document.getElementById('score');
var bestScoreLabel = document.getElementById('bestScore');
var video = document.getElementById('video');

function updTarget() {
	var correctStr = '';
	for (var i = 0; i < loc; i++) {
		correctStr += '_';
	}
	target.innerHTML = '<span id="space">' + correctStr + word.substring(loc) + '</span>';
}

function updTimer() {
	var timeLeft = start + limit - Date.now();
	timerLabel.textContent = (timeLeft / 1000).toFixed(2);
	var timeoutId = setTimeout(updTimer, 10);
	if (timeLeft < 0) {
		play = false;
		setTimeout(showScore(), 100);
		clearTimeout(timeoutId);
		timerLabel.textContent = '0.00';
		target.innerHTML = 'PRESS <span id="space">SPACE</span> TO REPLAY';
	}
}

window.addEventListener('keyup', function (e) {
	if (play === true) {
		return;
	}
	if (e.key === ' ') {
		play = true;
		limit = 10000;
		loc = 0;
		correct = 0;
		miss = 0;
		correctLabel.textContent = correct;
		missLabel.textContent = miss - 1;
		video.play();
		word = words[Math.floor(Math.random() * words.length)];
		updTarget();
		start = Date.now();
		updTimer();
	}
});
window.addEventListener('keyup', function (e) {
	if (play === false) {
		return;
	}
	if (e.key === word[loc]) {
		loc++;
		if (loc === word.length) {
			limit += 2000;
			word = words[Math.floor(Math.random() * words.length)];
			loc = 0;
		}
		correct++;
		correctLabel.textContent = correct;
		updTarget();
	} else {
		if (miss !== 0) {
			limit -= 500;
		}
		miss++;
		missLabel.textContent = miss - 1;
	}
});

function showScore() {
	video.pause();
	var beforeScore = score;
	score = correct - miss + 1;
	scoreLabel.textContent = score;
	if (beforeScore < score) {
		bestScoreLabel.textContent = score;
	}
}
